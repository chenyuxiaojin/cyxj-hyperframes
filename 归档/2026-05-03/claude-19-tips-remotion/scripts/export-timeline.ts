import fs from "node:fs";
import path from "node:path";
import {
  FPS,
  HEIGHT,
  VIDEO_END_MS,
  WIDTH,
  buildTipTimeline,
  loadFixedCaptions,
} from "../src/data/timeline";

const outputPath = path.resolve(
  process.cwd(),
  "src/data/timeline.generated.ts",
);

const captions = loadFixedCaptions();
const tips = buildTipTimeline(captions);

const generated = `// Generated by npm run generate:data. Do not edit by hand.
import type { TipNode } from "./timeline";

export const VIDEO_META = ${JSON.stringify(
  {
    fps: FPS,
    width: WIDTH,
    height: HEIGHT,
    durationMs: VIDEO_END_MS,
    durationInFrames: Math.ceil((VIDEO_END_MS / 1000) * FPS),
  },
  null,
  2,
)} as const;

export const TIP_TIMELINE: TipNode[] = ${JSON.stringify(tips, null, 2)};
`;

fs.writeFileSync(outputPath, generated);
console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
console.log(`Tips: ${tips.length}`);
console.log(`Duration: ${(VIDEO_END_MS / 1000).toFixed(3)}s`);
